"""
Operadores de Asignación
=  : Asigna el valor de la derecha a la variable de la izquierda.
+= : Suma y asigna (x += 2 equivale a x = x + 2).
-= : Resta y asigna (x -= 2 equivale a x = x - 2).
*= : Multiplica y asigna (x *= 2 equivale a x = x * 2).
/= : Divide y asigna (x /= 2 equivale a x = x / 2).
%= : Módulo y asigna (x %= 2 equivale a x = x % 2).
//= : División entera y asigna (x //= 2 equivale a x = x // 2).
"""

# Uso del Operador =
a = 10
b = 20
c = 30
d = 40
e = 50
f = 60

# Uso del operador +=
a += 5
print(a)

# Uso del operador -=
b -= 5
print(b)

# Uso del operador *=
c *= 3
print(c)

# Uso del operador /=
d /= 20
print(d)

# Uso del operador %=
e %= 20
print(e)

# Uso del operador //=
f //= 15
print(f)
