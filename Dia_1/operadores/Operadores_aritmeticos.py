"""
Aritméticos: Realizan operaciones matemáticas.
+: Suma.
-: Resta.
*: Multiplicación.
/: División.
//: División entera (redondea hacia abajo).
%: Módulo (resto de una división).
**: Potenciación.
"""

suma = 2+2
print(suma)

resta = 10-5
print(resta)

multiplicacion = 2*5
print(multiplicacion)

division = 20/10
print(division)

division_entera = 13//9
print(division_entera)

resto_division = 11%2
print(resto_division)

potencia = 2**10
print(potencia)
