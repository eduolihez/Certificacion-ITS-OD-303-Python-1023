"""
TIPOS DE DATOS
"""

# str = cadenas de texto representadas con "" o ''
var1 = str('Hola Mundo')
print(var1)

# int = numeros enteros (negativos incuidos) | Nunca compienzan con 0
var2 = int(1234567890)
print(var2)

#float = numeros decimales (negativos incluidos)
var3 = float(3.1416)
print(var3)

# bool = valores True / False
var4 = bool(True)
print(var4)
